# iBall op values
class Ops:
    TARGET_DEVICE = 0x00
    TARGET_ZONE = 0x01
    STORE = 0x02
    SETTIME = 0x04
    ENABLE_OUTPUT = 0x07
    X8 = 0x08
    REINIT = 0xFF


class Addr:
    ZERO = 0x0000

    DEVICE_BASE = 0x0100  # 0x0100-0x01ff
    DEVICE_ZONE_SERIAL = DEVICE_BASE + 0  # 0x0100-0x0103
    DEVICE_ZONE_CHANNEL = DEVICE_BASE + 4  # 0x0104
    DEVICE_ACTIVE_POSITION = DEVICE_BASE + 8  # 0x0108
    DEVICE_ACTIVE_SEQUENCE = DEVICE_BASE + 9  # 0x0109
    DEVICE_ACTIVE_BANK = DEVICE_BASE + 10  # 0x010a
    DEVICE_CHASE_STATE = DEVICE_BASE + 11  # 0x010b
    DEVICE_SCHEDULE_ENABLED = DEVICE_BASE + 13  # 0x010d

    DEVICE_BANK_REPEAT = DEVICE_BASE + 0x80 + 0  # 0x0180-0x0183

    SCHEDULE_ENTRY_MAX_INDEX = 0x01ff  # 0x01ff
    SCHEDULE_BASE = 0x0200  # 0x0200-0x11ff

    SEQUENCE_BASE = 0x2000  # 0x2000-0x20ff
    SEQUENCE_STATE = SEQUENCE_BASE + 0  # 0x2000
    SEQUENCE_REPEAT_COUNT = SEQUENCE_BASE + 1  # 0x2001
    SEQUENCE_POSITION_COUNT = SEQUENCE_BASE + 2  # 0x2002
    SEQUENCE_CHECKSUM = SEQUENCE_BASE + 3  # 0x2003-0x2004

    POSITION_BASE = 0x2100  # 0x2100-0x30ff
    POSITION_IMAGE_INDEX = POSITION_BASE + 0  # 0x2100 / 16
    POSITION_COLUMNs = POSITION_BASE + 1  # 0x2101 / 16
    POSITION_GAP = POSITION_BASE + 2  # 0x2102 / 16
    POSITION_BRILLIANCE = POSITION_BASE + 3  # 0x2103 / 16
    POSITION_SCROLL_SPEED = POSITION_BASE + 4  # 0x2104 / 16
    POSITION_OFFSET = POSITION_BASE + 5  # 0x2105 / 16
    POSITION_REPEAT_INDEX = POSITION_BASE + 6  # 0x2106 / 16
    POSITION_REPEAT_COUNT = POSITION_BASE + 7  # 0x2107 / 16
    POSITION_FLASH_INTERVAL = POSITION_BASE + 9  # 0x2109 / 16
    POSITION_WIPE_INCREMENT = POSITION_BASE + 9  # 0x2109 / 16
    POSITION_FADE_INCREMENT = POSITION_BASE + 10  # 0x210a / 16
    POSITION_FRAME_COUNT = POSITION_BASE + 15  # 0x210f / 16

    IMAGE_BASE = 0x3100  # 0x3100-0x34ff
    IMAGE_WIDTH = IMAGE_BASE + 0  # 0x310 / 4
    IMAGE_OFFSET = IMAGE_BASE + 2  # 0x310 / 4

    DATA_BASE = 0x3500  # 0x3500-0xbdff
